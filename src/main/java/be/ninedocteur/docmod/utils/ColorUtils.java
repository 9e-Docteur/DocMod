package be.ninedocteur.docmod.utils;

public class ColorUtils {

    public static int getWhite(){
        int white = 0xffffff;
        return white;
    }

    public static int getRed(){
        int red = 0xd40000;
        return red;
    }

    public static int getGreen(){
        int green = 0x00ff44;
        return green;
    }

    public static int getYellow(){
        int yellow = 0xffff00;
        return yellow;
    }
    
    public static int getOrange(){
    	int orange = 0xffa500;
    	return orange;
    }
}
